#include <stdio.h>
/*
 * [0][0] = 0 [0][1] = 5 [0][2] 10 [0][3] 15 [0][4] 20
 * [1][0] = 5 [1][1] = 10 [1][2] 15 [1][3] 20  [1][4] 25
 * [2][0] 10 [2][1] 15 [2][2] [2][3] [2][4] 30
 * [3][0] 15 [3][1] 20 [3][2] [3][3] [3][4] 35
 * [4][0] 20 [4][1] 25 [4][2] [4][3] [4][4] 40
 */

int main(){
	setbuf(stdout, NULL);

	int numbers[5][5];

	for(int i = 0; i < 5; i++){
		for(int j = 0; j < 5; j++){
			numbers[i][j] = (i+j)*5;
		}
	}

	for(int i = 0; i < 5; i++){
		for(int j = 0; j < 5; j++){
			printf("%d ", numbers[i][j]);
		}
		printf("\n");
	}

	return 0;
}
